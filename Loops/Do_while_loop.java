public class Do_while_loop {
    public static void main(String[] args) {

        int counter = 1 ; 
        
        do{
            System.out.println("Hello world!!!");
            counter ++  ; 
        }while(counter <= 10) ; 

        System.out.println("-=-=-=-=-=-===-===--");

        do{
            System.out.println("the list of movies : ");
            System.out.println("Harry Potter and the Chamber of Secrets (2002)");
            System.out.println("Harry Potter and the Prisoner of Azkaban (2004)");
            System.out.println("Harry Potter and the Goblet of Fire (2005)");
            System.out.println("Harry Potter and the Order of the Phoenix (2007)");
            System.out.println("Harry Potter and the Half-Blood Prince (2009)");
            System.out.println("Harry Potter and the Deathly Hallows – Part 1 (2010)");
            System.out.println("Harry Potter and the Deathly Hallows – Part 2 (2011)");
            System.out.println("--------------------------------------------------");

            counter ++ ;
        }while(counter <= 50) ;

    }
}
