public class Assignment_operators {
    public static void main(String[] args) {
        int number_1 = 25 ; 
        number_1 += 5 ;
        System.out.println(number_1);

        number_1 *= 2 ; 
        System.out.println(number_1);

        number_1 /= 5 ; 
        System.out.println(number_1);

        number_1 -= 3 ;
        System.out.println(number_1);

        number_1 %= 2 ;
        System.out.println(number_1);
    }
}
