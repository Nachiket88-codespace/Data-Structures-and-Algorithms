public class Relational_operator {
    public static void main(String[] args) {
        int number = 247 ; 
        float indice = 251 ; 

        System.out.println(number == indice);
        System.out.println(number != indice);
        System.out.println(number >= indice);
        System.out.println(number <= indice);
        System.out.println(number > indice);
        System.out.println(number < indice);
    }
}
